%methods (Access = private);
    % Xử lý sự kiện khi nút tính toán được nhấn
    function calculateButtonPushed(app, event)
        % Lấy giá trị từ ô nhập
        co = str2double(app.InputEditField.Value);

        % Tính toán Wl và Ws
        il = find(app.Liquid >= 1300);
        cl = il(1);
        is = find(app.Solid >= 1300);
        cs = is(1);
        Wl = (cs - co) / (cs - cl);
        Ws = (co - cl) / (cs - cl);

        % Hiển thị kết quả
        if Wl >= 1 
            resultMsg = sprintf('Wl = %.4f\n', Wl);
        elseif Ws >= 1
            resultMsg = sprintf('Ws = %.4f\n', Ws);
        else
            resultMsg = sprintf('Wl = %.4f\nWs = %.4f\n', Wl, Ws);
        end
        app.ResultTextArea.Value = resultMsg;
    end

